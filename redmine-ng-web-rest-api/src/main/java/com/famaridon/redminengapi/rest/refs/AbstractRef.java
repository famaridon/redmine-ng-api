package com.famaridon.redminengapi.rest.refs;

public abstract class AbstractRef {
	public Long id;
	public String name;
	
	public Long getId()
	{
		return this.id;
	}
	
	public void setId(Long id)
	{
		this.id = id;
	}
	
	public String getName()
	{
		return this.name;
	}
	
	public void setName(String name)
	{
		this.name = name;
	}
}
